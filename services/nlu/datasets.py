"""Dataset readers and lightweight HuggingFace dataset adapters."""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

from services.nlu.models import IntentSample, NERSample


class EncodedDataset:
    """Minimal dataset object accepted by HuggingFace Trainer."""

    def __init__(self, encodings: dict[str, Any], labels: list[int] | list[list[int]]) -> None:
        self._encodings = encodings
        self._labels = labels

    def __len__(self) -> int:
        return len(self._labels)

    def __getitem__(self, index: int) -> dict[str, Any]:
        item = {key: value[index] for key, value in self._encodings.items()}
        item["labels"] = self._labels[index]
        return item


def load_intent_splits(path: Path) -> dict[str, list[IntentSample]]:
    """Read intent JSON without coupling to dataset generation code."""

    with path.open("r", encoding="utf-8") as file:
        payload = json.load(file)

    splits: dict[str, list[IntentSample]] = {}
    for split_name, rows in payload["splits"].items():
        splits[split_name] = [
            IntentSample(text=str(row["text"]), intent=str(row["intent"]))
            for row in rows
        ]
    return splits


def load_ner_splits(path: Path) -> dict[str, list[NERSample]]:
    """Read CoNLL-style BIO data generated by Phase 1."""

    splits: dict[str, list[NERSample]] = {"train": [], "validation": [], "test": []}
    current_split = ""
    current_intent = ""
    current_text = ""
    tokens: list[str] = []
    tags: list[str] = []

    def flush_current() -> None:
        nonlocal tokens, tags
        if tokens:
            splits[current_split].append(
                NERSample(
                    tokens=tokens,
                    tags=tags,
                    split=current_split,
                    intent=current_intent,
                    text=current_text,
                )
            )
            tokens = []
            tags = []

    with path.open("r", encoding="utf-8") as file:
        for raw_line in file:
            line = raw_line.rstrip("\n")
            if not line:
                flush_current()
                continue
            if line.startswith("# split = "):
                flush_current()
                current_split = line.removeprefix("# split = ").strip()
                continue
            if line.startswith("# intent = "):
                current_intent = line.removeprefix("# intent = ").strip()
                continue
            if line.startswith("# text = "):
                current_text = line.removeprefix("# text = ").strip()
                continue
            token, tag = line.rsplit(" ", maxsplit=1)
            tokens.append(token)
            tags.append(tag)

    flush_current()
    return splits


def build_intent_dataset(
    samples: list[IntentSample],
    tokenizer: Any,
    label_to_id: dict[str, int],
    max_length: int,
) -> EncodedDataset:
    texts = [sample.text for sample in samples]
    labels = [label_to_id[sample.intent] for sample in samples]
    encodings = tokenizer(
        texts,
        truncation=True,
        padding=True,
        max_length=max_length,
    )
    return EncodedDataset(encodings=encodings, labels=labels)


def build_ner_dataset(
    samples: list[NERSample],
    tokenizer: Any,
    label_to_id: dict[str, int],
    max_length: int,
) -> EncodedDataset:
    token_rows = [sample.tokens for sample in samples]
    tag_rows = [sample.tags for sample in samples]
    encodings = tokenizer(
        token_rows,
        is_split_into_words=True,
        truncation=True,
        padding=True,
        max_length=max_length,
    )
    labels = [
        align_bio_tags(encodings.word_ids(batch_index=index), tags, label_to_id)
        for index, tags in enumerate(tag_rows)
    ]
    return EncodedDataset(encodings=encodings, labels=labels)


def align_bio_tags(
    word_ids: list[int | None],
    tags: list[str],
    label_to_id: dict[str, int],
) -> list[int]:
    """Label only the first subword; mask continuations for cross-entropy."""

    label_ids: list[int] = []
    previous_word_id: int | None = None
    for word_id in word_ids:
        if word_id is None:
            label_ids.append(-100)
        elif word_id != previous_word_id:
            label_ids.append(label_to_id[tags[word_id]])
        else:
            label_ids.append(-100)
        previous_word_id = word_id
    return label_ids
